        <h5>Randy Brecker</h5>
        <img class="headshot" src="/images/portraits/randy.jpg" alt="Randy Brecker" />  
        <p>
            Born in Cheltenham, Pennsylvania, Randy Brecker studied trumpet at Indiana University. He became a seminal member of <em>The Eleventh House</em> in 1973. Together with his younger brother, the late jazz saxophonist Michael Brecker, Randy led bands such as Dreams and the Brecker Brothers from the 1970s through the 1990s and later recorded and toured with Jaco Pastorius' Word of Mouth big band.
        </p>
        <p>    
            Today, Randy is a sought after session musician and has performed or recorded with such musicians as: Billy Cobham; Bruce Springsteen; Lou Reed; Charles Mingus; Blood, Sweat &amp; Tears; Frank Zappa, Parliament-Funkadelic, Dire Straits, Spyro Gyra, and many others.
        </p>
      </section>
    
      <section id="mouzon" class="six columns">
        <h5>Alphonse Mouzon</h5>
        <img class="headshot" src="/images/portraits/alphonse.jpg" alt="Alphonse Mouzon"/>
        <p>
           Alphonse Mouzon was born in Charleston, South Carolina. He studied drama and music at the City College of New York and medicine at Manhattan Medical School and played in the jazz fusion band, Weather Report. He joined <em>The Eleventh House</em> in 1973 and helped to put the band on the map.
        </p>
        <p>
            Mouzon has performed and recored with the likes of Herbie Hancock, Miles Davis, Stevie Wonder, Eric Clapton, Jeff Beck, Carlos Santana, Jaco Pastorius and more. He currently lives in Northridge, California with his daughter and pet Shih Tzu, Princess.
        </p>
      </section>
