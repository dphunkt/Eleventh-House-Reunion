        <h5>Mike Mandel</h5>
        <img class="headshot" src="http://placehold.it/350x245" alt="Mike Mandel" />
        <p>
            Mike Mandel grew up in rural Yakama, Washington less than 50 miles from his childhood friend Larry Coryell and the two formed the band the Checkers in 1960. While living in New York, Mandel joined <em>The Eleventh House</em> in 1974 lending a funky, synthesizer-driven style to many tracks.
        </p>
        <p>
            Mandel recorded in the late 1970s with some of the hottest musicians in the New York jazz scene, including Steve Khan, George Young and David Sanborn. Mandel is blind since early childhood and took up the piano. He currently resides in New York City and is actively involved in the music scene.
        </p>
      </section>
      
      <section id="trifan" class="six columns">
        <h5>Danny Trifan</h5>
        <img class="headshot" src="http://placehold.it/350x245" alt="Danny Trifan" />  
        <p>
            Danny Trifan was born in Missouri. He joined <em>The Eleventh House</em> in 1972 and played on all six of their recored albums. In 1976 he joined Blood Sweat &amp; Tears and more recently played with musicians such as David Sandborn and Tom Waits. In 2001, Trifan reunited with Coryell to play at St. Louis Jazz Festival 
        </p>
        <p>
            Trifan chose to pursue his other passion, History and earned his Ph.D from Duke University. Today, Trifan is a Professor of History at Missouri Western State University.
        </p>
