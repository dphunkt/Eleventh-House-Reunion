        <h5 class="twelve columns">Larry Coryell</h5>
        <div class="six columns">
        <img src="/images/portraits/larry.jpg" class="headshot" alt="Larry Coryell"/>
        </div>
        
        <div class="six columns">
            <p>
                Coryell was born in Galveston, Texas and attended the University of Washington. Coryell moved to New York City in 1965 and became a pioneer of "jazz fusion". He formed his own group, <em>The Eleventh House</em>, in 1973 and the ensemble toured widely in America and abroad.
            </p>
            <p>
                In 1979, Coryell formed <em>The Guitar Trio</em> with jazz fusion guitarist John McLaughlin and flamenco guitarist Paco de Lucia. The group toured Europe briefly, eventually releasing a video recorded at Royal Albert Hall in London entitled <em>Meeting of Spirits</em>.
            </p>
            <p>    
                In 2007, Coryell published an autobiography titled <em>Improvising: My Life in Music</em>. Coryell continues to tour and play music worldwide.
        </p>
        </div>
    </section>
</div>

